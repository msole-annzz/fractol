void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	char	*str;

	i = 0;
	str = s;
	if (!s || !f)
		return ;
	else
		while (str[i] != '\0')
		{
			(*f)(&str[i]);
			i++;
		}
}
