void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	char			*str;

	str = s;
	i = 0;
	if (!s || !f)
		return ;
	else
		while (str[i] != '\0')
		{
			(*f)(i, &str[i]);
			i++;
		}
}
